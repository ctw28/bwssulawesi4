
<h2 class="judul-section side biru">Kenggotaan TKPSDA</h2>
	<p style="text-align:justify; line-height:25px;">Tim Koordinasi Pengelolaan Sumber Daya Air Wilayah Sungai Lasolo Konaweha ditetapkan melalui surat Keputusan Menteri Pekerjaan Umum 
	Nomor 25/KPTS?M/2012 tanggal 07 Februari 2012 tentang pembentukan <b>Tim Koordinasi Pengelolaan Sumber Daya Air Wilayah Sungai Lasolo â€“ Sampara</b> yang kemudian diubah dengan 
	Surat Keputusan Menteri Pekerjaan Umum Nomor 436/KPTS/M/2012 tanggal 26 Desember 2012 tentang pembentukan <b>Tim Koordinasi Pengelolaan Sumber Daya Air Wilayah SungI Lasolo-Konaweha.</b> 
	Dimana lembaga ini merupakan Organisaasi Non Struktural, berada dibawah dan bertanggung jawab langsung kepada Menteri Pekerjaan Umum.<br><br>Keanggotan lembaga ini meliputi 50% unsur. 
	Pemerintah atau 17 (tujuh belas) anggota, yaitu;6 (enam) dari Prov. Sulawesi Tenggara, 2 (dua) dari Prov. Sulawesi Tengah, 2 (dua) dari Kab.Konawe, 2 (dua) dari Kab.Kolaka, 1 (satu) dari 
	Kab.Konawe Utara, 1 (satu) dari Kab.Konawe Selatan, 2 (dua) dari Kab.Morowali dan 1 (satu) dari Pemerintah Pusat.</p>

	<p style="text-align:justify; line-height:25px; ">
	Sedangkan 50% lainnya berasal dari unsur Non-Pemerintah yang terdiri dari 17 (tujuh belas) anggota, yaitu; 2 (dua) dari Lembaga Adat, 5 (lima) dari Gabungan Petani Pemakai Air, 
	2 (dua) dari Persatuan Perusahaan Air Minum, 2 (dua) dari LSM, 1 (satu) dari Kadin, dan 2 (dua) dari Organisasi Tani.</p>

