<h2>Visi dan Misi</h2>
<h3>Visi</h3>
<p>"Terwujudnya kemanfaatan sumber daya air yang berkelanjutan untuk sebesar-besarnya kemakmuran rakyat"</p> 

<h3>Misi</h3>
<ul class="list-style-number">
	<li>Melaksanakan Konservasi Sumber Daya Air yang Berkelanjutan</li>
	<li>Pendayagunaan Sumber Daya Air yang adil untuk memenuhi syarat kualitas dan kuantitas</li>
	<li>Pengendalian daya rusak air</li>
	<li>Pemberdayaan dan peningkatan partisipasi masyarakat, swasta dan pemerintah dalam pengelolaan dan pembangunan Sumber Daya Air</li>
	<li>Peningkatan keterbukaan dan ketersediaan data serta informasi dalam pembangunan Sumber Daya Air</li>
</ul>