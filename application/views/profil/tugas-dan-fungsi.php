
<h2 class="judul-section side biru">Unit Kerja Balai Wilayah Sungai Sulawesi IV Kendari</h2>
	
	<h2 class="judul-section side hijau">Subbagian Tata Usaha</h2>
		<p style="text-align:justify">Subbagian Tata Usaha mempunyai tugas melakukan urusan administrasi kepegawaian, organisasi, dan tatalaksana, pelaksanaan fasilitasi kegiatan Reformasi Birokrasi di Balai, 
		penyusunan rencana dan pengelolaan urusan kas dan perbendaharaan, administrasi dan akuntasi keuangan, administasi dan fasilitasi penyelesaian hasil pemeriksaan dan pengaduan masyarakat, 
		pelaksanaan pemungutan, penerimaan dan penggunaan biaya jasa pengelolaan sumber daya air (BPJSDA), urusan tata usaha, kearsipan dan rumah tangga serta pelaksanaan komunikasi publik, 
		melakukan penatausahaan, pengelolaan, administrasi dan akuntansi barang milik negara, pengamanan fisik barang milik negara, pengelolaan kekayaan negara lainnya, serta koordinasi kegiatan 
		terkait pengadaan lahan.</p>

	<h2 class="judul-section side hijau">Seksi Perencaan Umum dan Program</h2>
		<p style="text-align:justify">Seksi Perencanaan Umum dan Program melakukan penyiapan bahan penyusunan pola pengelolaan sumber daya air, rencana pengelolaan sumber daya air, program jangka menengah pengelolaan 
		sumber daya air, analisis dan evaluasi kelayakan program, dan pengelolaan sistem informasi dan data sumber daya air, serta fasilitasi penerepan sistem manajemen mutu, penyusunan rencana 
		program dan penganggaran kegiatan tahunan pengelolaan sumber daya air wilayah sungai, analisa kesiapan usulan kegiatan dan skala prioritas penganggaran, analisis mengenai dampak lingkungan, 
		fasilitasi penerapan sistem manajemen keselamatan dan kesehatan kerja (SMK3), fasilitasi pengadaan barang dan jasa, pelaksanaan pemberdayaan masyarakat di bidang program dan perencanaan umum, 
		serta penyusunan perjanjian kinerja dan laporan kinerja balai.</p>

	<h2 class="judul-section side hijau">Seksi Pelaksanaan</h2>
		<p style="text-align:justify">Seksi pelaksanaan melakukan penyiapan bahan penyusunan rencana kegaiatan, serta pengendalian dan pengawasan pelaksanaan perancanaan teknik, pelaksanaan konstruksi dan non konstruksi, 
		persiapan penyerahan operasi dan pemeliharaan, serta fasilitasi penerapan sistem manajemen mutu dan sistem manajemen keselamatan dan keseharan kerja (SMK3), fasilitasi pengadaan barang dan 
		asa, pelaksanaan pemberdayaan masyarakat di bidang sungai, pantai, drainase utama perkotaan, irigasi, rawa, dan tambak, air tanah dan air baku, bendungan, danau, situ, embung dan tampungan 
		air lainnya, serta konservasi tampungan air, sungai dan pantai, serta air tanah dan air baku.</p>

	<h2 class="judul-section side hijau">Seksi Operasi dan Pemeliharaan</h2>
		<p style="text-align:justify">Seksi Operasi dan Pemeliharaan penyiapan bahan penyusunan rencana kegiatan, fasilitasi penerapan sistem manajemen mutu, pengelolaan sistem hidrologi dan sistem peringatan dini, 
		koordinasi pengelolaan sumber daya air dan penyelenggaraan pemantauan dan pengawasan penggunaan sumber daya air serta penyelenggaraan penyidikan tindak pidana bidang sumber daya air, 
		pengendalian dan pengawasan pelaksanaan perencanaan teknik, persiapan pelaksanaan operasi dan pemeliharaan, pelaksanaan operasi dan pemeliharaan, dan pelaksanaan penanggulangan kerusakan 
		akibat bencana serta fasilitasi penerapan sistem manajemen keselamatan dan kesehatan kerja (SMK3), fasilitasi pengadaan barang dan jasa, serta pelaksanaan pemberdayaan masyarakat di bidang 
		operasi dan pemeliharaan.</p>

